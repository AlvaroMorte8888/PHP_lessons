// Факториал - это произвеение всех целых чисел, меньших или равных даному числу!!!

function factorial($num) {
  if ($num <= 1) return 1;
  // здесь у нас происходим повторный вызов функции
  return $num * factorial($num - 1);  
}

//echo factorial(8);

function factorial2($num) {
  $result = 1;
  for ($i = 1; $i <= $num; $i++) {
    $result *= $i;
  }
  return $result;
}
echo factorial2(8);

















