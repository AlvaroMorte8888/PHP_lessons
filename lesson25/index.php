// Рекрусивные функции - функция вызывает саму себя!!

function recur($num) {
  // Если параметра $num меньше 10, продолжаем рекурсию
  if ($num >= 10) {
  // Уменьшать значение параметра $num и его выводим в браузер
    echo ($num --) . "<br>";
    // Производим рекрсивный вызов функции
    recur($num);
  }else return;
}

recur(15);


